class ProductFeature extends Model
{
    use SoftDeletes;

    public $table = 'product_features';

    const CREATED_AT = 'created_at';
    const UPDATED_AT = 'updated_at';


    protected $dates = ['deleted_at'];


    public $fillable = [
        'code',
        'price',
        'iva',
        'product_feature_category_id',
        'status_id'
    ];

    /**
     * The attributes that should be casted to native types.
     *
     * @var array
     */
    protected $casts = [
        'id' => 'integer',
        'code' => 'string',
        'price' => 'float',
        'iva' => 'float',
        'product_feature_category_id' => 'integer',
        'status_id' => 'integer'
    ];

    /**
     * Validation rules
     *
     * @var array
     */
    public static $rules = [

    ];

    /**
     * @return \Illuminate\Database\Eloquent\Relations\BelongsTo
     **/
    public function productFeatureCategory()
    {
        return $this->belongsTo(\App\Models\Admin\ProductFeatureCategory::class);
    }

    /**
     * @return \Illuminate\Database\Eloquent\Relations\BelongsTo
     **/
    public function status()
    {
        return $this->belongsTo(\App\Models\Admin\Status::class);
    }

    /**
     * @return \Illuminate\Database\Eloquent\Relations\HasMany
     **/
    public function productFeatureTranslations()
    {
        return $this->hasMany(\App\Models\Admin\ProductFeatureTranslation::class);
    }

    /**
     * Return the ProductFeatureTranslation in current languague.
     *
     * @return ProductFeatureTranslation
     **/
    public function productFeatureTranslation()
    {
        $language = Language::where('code', \App::getLocale())->first();
        return $this->productFeatureTranslations->filter(function($productFeatureTranslation) use($language){
            return $productFeatureTranslation->language_id == $language->id;
        })->first();
    }
}
